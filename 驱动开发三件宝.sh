    设备模型之外，对于驱动程序的开发者来说，有三样东西是不可缺少的：第一是协议或标准的spec，
也就是规范，比如usb协议规范；第二是硬件的datasheet，即你的驱动要支持的硬件的手册；第三
就是内核里类似驱动的源代码，比如你要写触摸屏驱动的话，就可以参考内核里已经有的一些触摸屏驱动。
