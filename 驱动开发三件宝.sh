    spec、datasheet、内核源代码这三样东西对于每个开发设备驱动的人来说都是再寻常不过了，
但正是因为它们的普通，所以在很多人眼里都被归为被忽视的群体。于是大家开发驱动的过程中，
遇到问题的时候首先想到的可能还是“问问牛人怎么解决吧”、“旁边要是有个牛人该多好”，因为
牛人的稀有，所以知道牛人的价值，而又因为spec、datasheet和内核源代码的唾手可得，所以
常常体会不到它们在解决问题时的重要性。
