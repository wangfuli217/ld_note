【5】是/否徽章：开口说是/否(抱歉)以及真诚表达的能力。
1 撒特的温柔拒绝法：如果你不想做，那就简单说它不适合你，不要找借口。
( 你的借口很可能会因客户的进一步询问导致更糟糕的局面)
2 戈登的初次咨询法则：对于客户的第一次提议，不说不也不说是。
(不要对客户的提议说“是”，而是对考虑这一提议说“是”)
3 马修的是/否信号：诚实的人无须通过声明自己诚实可靠来证明自己所说的是与否。
(好好反省下自己在表达是/否的时候是否带有某种自我宣扬的意图)
4 老好人指南：如果你一定要让每个人都喜欢你，那就离开咨询界吧。
5 是/否规则的转换：将规则转换为行动指南时可以增加可选择的内容，而不是被迫接受。
(强调规则的灵活性，比如“永远不要接受馈赠”，但有时候如果你不接受仅仅是一个象征友谊的小礼物，对方会很不高兴。)


【6】心：专心而聪明地进行工作的能力和愿望，而不只有好的态度。
1 人的斜边假设：当甲隔离了你的心A和另一个人的心B，那么请选择A与B之间的最短距离。
(间接表达可能反而伤害别人感情。而阻碍直接交流却常常源于我们自身“心”的回避)
2 被激活的“心”：必须倾听自己的步伐，有意识地去做。
(我的理解是不要因为怕伤害对方而刻意回避，可以通过一些“模糊”的交流获取信息)
3 并行悖论：与客户太相似，你就不能吸引他们；与客户差太远，你又会将他们吓跑。
(更好的方法是只需保持自我)
4心的测试：如果你不关心他们或他们的问题，那么就不要为他们作咨询。
5生存规则：与其拼死努力，不如或者成功。
( 凡事必须有限度，如果有人请求你拼死相助，你就不会想帮助他)

【7】镜子：审视自我以及寻求和利用反馈的能力。
1 三指规则：指责他人时，请注意你的其他三个指头指向何方。
(与其立刻给出第一反应，不如三思而行)
2 莫妮卡奇妙的镜子：合同谈判对双方都是一个审视自我的绝佳时机。
(合同谈判时一个提出反馈意见的绝佳时机)
3 莫妮卡的镜子忠告：有些人根本不想去着非凡的镜子。
(并不是每个人都喜欢利用镜子自省)
4斯威尼的小签名法则：想获得一个有意义的承诺，在书面合同上签字。
5 帮助模式：不管表面看来如何，每个人都在努力想对事情有所帮助。
(把对方反馈意见(可能另你不快)当成一种提醒，而不是责备。)
6 肯尼的汽车修理定律：一贯需要修理或替换的部分总是藏在最难到达的位置。
( 你最需要持续调整或总是出问题的部分也总是那些你最不容易接触到的部位。)
7 一号秘密：根本不存在秘密。因为每个人都可以观察人类行为。
8 撒特的三个通用问题：(工作前用，有利于掌握全景)
1过去：我怎样达到？(我是如何联系到客户的)
2现在: 我在这里感觉如何？
3将来：我希望发生什么？(为什么接受这项目？成功是什么？我想在这里干多久？)


【8】望远镜：提醒我超越肉眼和大脑的局限来了解他人，并给予他们更多的理解。
1还是撒特的三个问题：(这次是针对客户)
2对他人产生偏见时，使用望远镜聚焦清单：自我感觉、环境、他人的记录、与自己认识的其他人有相似之处而产生错误印象？、投影、分类、心灵感应、合作历史、传闻、分享
(我看到或者听到的哪些特定内容导致我对此人产生了那种印象？)

【9】鱼眼镜：对自己和他人所处环境的观察和分析能力
1 伊莎贝尔最初的暗示：开始时你所面临的永远不会是一张白纸。
(你要了解客户的背景而非从0开始)
2 集中法则推论：对一个人是蜜糖，对另一个人可能就是毒药。
3 高效咨询第一法则：如果某种方法不起作用，那就试试别的法子。
(找出客户的偏好，是概念？数字？公式？图表？)
4 Don的偏差推导：如果太规则，它就不是观察而得的自然数据；而是被设计出来的结果。
(机械措施的强制下仍然可能会有因为个性而产生的结果)
5 解决问题定律：你越接近于发现谁是引起问题的原因，问题被解决的可能性越小。
(应该使用分离变量法，将问题本身和谁该因问题受到责备分开)
6 背景障碍：鱼儿总是最后一个看到水
(当我们考虑问题的时候，我们总是倾向于从思考中忽略掉那些我们已经习惯了的东西。)
7前景障碍：鱼儿总是第一个看到空气
(如果人们已经习惯了周围环境中的平常事物，他们就会去关注自己不习惯的事物 )
8 5分钟规则：客户们总是知道如何解决自己的问题，而且总是在交流的最初5分钟内说出解决方案。
9 弯曲信道之谜：如果收到的信息很难于理解，请首先检查信息渠道是否通畅
(强调一个清洁环境的重要性)

【10】陀螺仪：使自己的内心情感和外在行为保持一致或平衡的能力。
1 完美平衡悖论：如果你想保持不协调状态，那就努力去实现完美的协调一致吧。
(平衡只是个过程，而不是位置，它并不是完美无瑕)
2 体脑指示：首先要信任你的身体，然后再信任你的头脑
(身体直接反应现实，头脑不过是诠释身体所传达出来的感觉)
3 帕森特异原则：如果他们行为古怪，那很可能是他们对某件奇怪之事的反应；也许正因为我。
4 进行调整： 放松身体、用“我”来开始评论(“你”字有责备含义)、耐心等候对方回答。
5 输赢学习定理：如果你保持协调一致，虽然输赢皆有可能，但总会有收获。
6 魔力模式：权威咨询师所说的话容易被过分夸大
(你越协调一致，就越需要注意自己的言语)
7 代理综合症：不管你或他们多么想做，你也无法代替别人达到协调一致。
(只有客户在场边学习的时候，咨询师才会去示范如何去做)
8 知觉至上论：经历不只是最好的老师，还是唯一的老师。
(但它不一定见得会教你什么，对协调一致的追求练习必须在安全、有利于接纳知识、有帮助的环境之中)


【11】蛋、登山钩、羽毛——额外的工具
1蛋：从自身吸收需要的全部营养成分来成长、发展和学习的能力
(提醒我们每个人生来都具有那些工具，而且我们身长还隐藏着无数未知的程序，我们甚至可以自己去创造新的工具，学无止境。
2登山钩：确保自身安全和避免无谓风险的能力。
(每一个可能跌倒或失败之处，举出一个返例来进行反驳——帮助客户走出双重困境
注意安全、选择权和创造性三者的关系)
3 羽毛：逗自己和他人发笑以及轻松对待事物的能力。
(既然有些事情对大局无关紧要，不如轻松看待，不要太在意，保持乐观)


【12】沙漏——为重要事情抽出时间和充分利用时间的能力
1 时间管理的沙漏的组成部分：
a浪费时间：混乱、急躁、草率下结论、缺乏计划、错误、缺乏耐心、拒接接受现实、糟糕的组织、不合理的期望、打破自己的原则
b节省时间：信任、合理运用金钱、备份、留有余地、可靠的人、教导新人、测试、培训(尽早)、理性。
2 杰里项目生涯铁的规则：它总会花更长的时间
(做好延误的贮备，准备好适应突变的情况，宽容对待意外出现的延迟)
3定时炸弹：所有恶性最终都将得到报应
(草率行事是浪费时间的“最可靠”办法)
4 懒惰原则：绝对不要在今天做那些明天可能不必做的事；事实上，连想都不用想
(PS:当发现自己正在做时间的事情时，立刻停止)
