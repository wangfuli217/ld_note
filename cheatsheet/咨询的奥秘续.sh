二 咨询工具箱
【1】智慧宝盒——分辨什么适合我以及什么不适合我的能力。
1 集中原则：要想学到东西，就不能试图样样都学。(选出重点会事半功倍)
2 雷丁的阅读原则：绝对不要阅读任何不值得阅读的内容。(只要不去阅读无用的内容，阅读速度的提高没有什么意义)
3 卡里的废物警告：任何不值得做的事情都不值得把它作好。(不要掉入完美主义陷阱，把垃圾进行高质量的美化工作)
4 弗里伯的直觉过滤器：无论何事，只要不该做，就不去做。(不受他人的影响，要相信自己智慧宝盒做出的选择)
5 适度性规则：不要过于理性；要通情达理(理性过于严谨，而通情达理依靠合理、合情、直觉、数据等作出判断，同样可靠)
6 谨防理性化：听起来明智的事情并不见得都真正明智(有些纯粹的理性化产物，却被误认为是合理的意见)
7 唯利是图座右铭：损失大量钱财的一个最佳途径就是纯粹为了钱而做事。(要考虑钱，但同时也要留心其他方面的事情。 )
8 傻子综合征：无所不知的人最容易上当。
9 最重要的格言：不知道(如何解决)很可能并不会对你有害，但不记得总会有。(对于不易察觉的环境时，应该嵌入检查点，定期检查智慧宝盒，防止遗忘它)

【2】 金钥匙——开辟新领域和放弃不适时领域的能力。
1 波兰斯基指示器：如果人们非常确定问题不在某处，那么问题很有可能就在那儿。(人们常常因为盲目自信而不去检查那些自己认为没有问题的地方)
2 波兰斯基推论：不必在所有人都指向的地方寻找。(如果真的知道在何处寻找，人们就不会请咨询师来帮忙找到问题所在)
3 丹尼决策法：当不再学到新东西时，就该换换方向了。(咨询师之所以为咨询师是因为他掌握了别人所不知道的东西)
4 顾客的锁语：例如：禁止你查看XX，不然他们会不高兴的(挖掘紧缩大门背后的信息，比如询问他们是谁。避免无休止的提问)
5 催眠词语：语意不清，容易被解释任何含义。(例如“应该”，很可能被解释为“本应，实际没有”，类似的还有尽快、only、all、anything)
6 暗示永恒不变的评论——咨询师最危险的锁语，如我是咨询师(在永恒不变的评论前加上“ 到目前为止….”)

【3】勇气棒——尝试新鲜事物和冒险的能力。
1 懦夫的信条：勇气不是一种感觉，而是一种外在表现。(勇气其实是冷静和谨慎计划的结果，恐惧=发现一切可利用的资源)
2 Fraidycat 规则：如果对事物A的恐惧大于事物B，那么你会选择事物B。(如何让客户选择A：增加B的恐惧、减少A的恐惧、采取出乎客户的意外行动)
3 不要照章办事(照章办事的原因：照章办事即使错了，也很少受到惩罚)

祈愿杖：争取得到自己所想要的和必要时接受无法获得的结果的能力。
1 在明白自己愿望是什么之前，不要担心它们是否可行、合理、能被接受。
2 和客户的经纪人谈判不可能改善你的处境，因为他们是专家而你是业余的。(解决办法，雇佣一个律师或职业调解人去和对方谈判，不要雇经纪人)
3 快乐定理：如果你对代理的份额不满，他们就会因此而受穷。(当你对合同一无所知时，千万不要签订，谈判之前可以找客户曾经的承包商交流)
4 终极抑郁定理：如果不知道自己想要干什么，你就不大可能得到它。( 抵制诱惑，不要签订合同期过长的约束性合同)


【4】侦探帽(&放大镜)：研究数据和数据分析推理能力。
1 铁路逆向悖论：如果某项服务过于出色，服务商就很可能永远听不到意见，因而他们将会取消此项服务。
(许多咨询师误把客户的沉默当成对其服务的不满)
2 家庭主妇谬论：家庭妇女唯一重要的事情就是呆在家里等候上门服务。
(千万不要假定客户除了再电话旁等你的电话外没有更好的事做)
3 斜边假设：当三角形阻碍了你获得数据资料，请选择使用其斜边。
(我看到或听到的哪些特定内容导致我对此人产生了那种印象)
4锤子规律：孩子若收到锤子作为圣诞礼物，就会发现每件东西都需要捶打。
(你的数据会倾向于你擅长的调查方式)
5韦纳图书馆定律：没有答案，只有交叉引用和相互参照。
(避免成为锤子规律的牺牲者，应尽可能用多种方法收集数据)
6 莱吉恩规律：如果行动开始变得徒劳，那就去搜索信息。如果信息也变得徒劳，那就去睡一觉。
(收集数据时遇到麻烦的时候使用这一规律)
7 危机偏差：一旦身处险境或心存疑问，就开始团团乱转和大喊大叫。
(身处险境易使咨询师收集到一些无用数据或捏造数据)
