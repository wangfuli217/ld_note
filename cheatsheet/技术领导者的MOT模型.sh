第零章 简述

成为领导不是靠运气，而是靠投入。

技术领导者的全面模型（MOT）：突出'选择'与'发现'
M：激励(motivation)——奖励或是磨难，用来激励或惩罚相关的人
O：组织(organization)——现实的组织结构，将想法化为实践
I ：头脑或创新(ideas or innovation)——种子，对未来图景的想象

子模型一：解决问题
1. 理解问题（understanding the problem）
2. 管理思维（managing the flow of ideas）
3. 保证质量（mantaining quality）

子模型二：创新
1. 创新的三大障碍
1）不能从旁观者的角度去认识自己。
2）没问题综合症（NPS），欺骗自己，以为早就知道所有问题的答案
3）教条主义，认为一个问题只有一种解决办法
 
2. 提升创新能力：
1)任何实际问题都存在另一个解决办法，只是目前没人知道；
2)创造性的错误：错误是机会，是想法
3)希望保密的想法通常并不值得偷窃，值得偷窃的想法通常是被客户认为无关紧要的想法
4)综合：将两人的争论综合成第三条路
 
子模型三——激励：始终真诚（无论是否刻意）
I 交流
1. 交流可能导致的结果
1）开创新的可能
2）强化旧的可能
3）激活生存规则
4）增强攻击性
5）加重痛苦
 
2. 交流的过程：假定消息的真正内容为A
1）感官接受（sensory input）：接受过程可能会丢掉一些信息，信息的表达内容也会受到信息表达方式的影响，消息变成了B
2）解释（interpretation）：根据自己的经历对B而不是A进行解释，结果是可能导致多种解释,消息变成了C
3）感受（feeling） ：对C产生了某种感受D
4）对感受的感受（feeling about the feeling）：对感受D的反应变成了E
5）防卫（defense）：选择是否进行防卫？选择哪种防卫方式？F
6）发表意见的规则（rules for commenting）：根据自己的一些准则决定回应内容G
7）输出（outcome） ：输出的消息H实际上掺杂了B-G

3. 导致交流发生问题的5个主要原因：
1）接受差异：双方没有以同样的方式接受表层交流，因为人不同，接受也不同
2）时间错误：转换涉及过去或未来发生的事情，这些事情与正在进行的交流没有逻辑关系
3）地点错误：转换涉及一些其他的情景
4）人物错误：这种转换关涉到其他人
5）自我评价：我关于自己的感觉，对于我的反应有很强的影响，但是你并不能直接知道我的这种感受，无论是平时还是现在
 

II 帮助：十大经验法则
1）关涉到生存问题时，必须把人放在第一位，别无选择
2）如果不是高度技术化的工作，领导者就可以通过威慑来领导，而不需要太强的能力
3）有强大技术背景的人可以吧任何任务斗转化为技术任务，以此逃避他们不想做的事情
4）不关心人的领导没有可以领导的人，除非手下人别无选择
5）如果你不能提供什么实质性内容，却要假装有内容，那么无论你多么关心别人，都不会吸引住他们
6）面向任务的领导往往高估了自己的成就
7）我们所做的工作，几乎无一真正重要到必须牺牲执行者的未来
8）如果任务很复杂，就没有哪位领导能够担保计划不会'百密一疏'。（也就是说在复杂环境下，大多数任务优先的领导也必须把人放在首要位置，否则就不可能完成任务）
9）要成为成功的技术领导，就不能对任何人的人性有丝毫的抑制
10）如果你是领导，以平常心对待他人就是你的任务。在此之外，没有其他值得完成的任务。
 

子模型五：有效组织
1. 有效组织的三大障碍：
1）抓大目标：忙于做大事，没有时间来做那些组织本来应该做的事情
2）把人当机器：人非机器，可能按照你的表达方式去做而非按照表达意思去做
3）亲自动手：领导者的任务是创造环境，而非替他人完成任务（除非关系存亡）
4）奖励低效的组织：真正的组织是缺乏艺术和激情的，我们热衷奖励那些问题不断，但决策经验丰富的组织而非那种能够避免问题的组织（沉静型领导）

2. 学习成为组织者：
1） 观察 —试验 —寻找差异
2） 入门级观察者通常会混淆真正的组织结构和表面上的权利结构

3. 有效组织的形式：
1） 各自独立解决（Individually）
2） 投票（Voting）
3） 强势领导（Strong leader）
4） 一致意见（Consensus）

子模型六：个人成长
1. 螺旋式循环上升
1）经历高原：慢速增长
2）经历低谷
3）从低谷到新的高原：实现快速增长

2. 了解自我：写日志
1）我的情况如何
2）我每天做什么
3）我的感觉如何
4）我如何发现他人对我的反应
 
3. 描绘事业线，量化你的事业，在时间轴上标出你的人生曲线，可以得到以下结论：
1）重要的不是事情，而是你对事情的态度。
2）成功可能滋生失败
3）每个成功的技术领导都具有远见：拥有你才能贡献的特殊成分 ，是培养眼见的关键
4）缺乏远见的人对他人没有什么影响力
 
4. 正确看待力量
1）力量的来源：技术、关系、专业技能
1）从关系的角度看待力量
2）在遇到一个获得力量的机会，先问问自己到底要用这种力量干什么
3）在组织中，完成力量的转换
4）避免专业力量所带来的锤子效应
5）成熟强调的是交际和沟通的能力，技巧则是次要的


5. 你的支持系统：支持是互惠的
1）来自技术资源支持
2）来自批评的支持
3）对个人成长的支持
4）对个人健康的支持
5）对情绪的支持：你倾诉的对象
6）对精神的支持

6. 从元规则到指导准则
1）界定规则（我必须帮助所有人）
2）找到这条规则与自己无意识心理相协调的理由，每一步做出微小的改变，直到新规则形成
3）将命令转化为选择（我可以时刻帮助任何人）
4）从确定性到可能性（我可以在某些时刻帮助任何人）　　
5）从通用到非通用（我可以在某些时刻帮助某些人）
6）从普遍到特殊（我能在某个时刻帮助某些人，当…）

形成最终规则：我能在某个时候帮助某些人，他们明确要求我的帮助，我有能力帮助他们，我有资源帮助他们，帮助他们让我感觉很好，我选择帮助他们，我能承受帮助失败的结果



第一章 领导力究竟是什么？
    所谓领导力，就是创造这样一个环境，每个人都能在其中发挥更多的能力。

    领导力模型
    全面(organic)模型: 系统化思维 —— 某一事件必然是包括时间在内的千百种因素共同作用的结果；
1. 价值在于：能够从容面对自己并未充分理解的复杂形势。
2. 根据人的特殊性来定义人，容许各种各样的人找到复杂环境中共事的基础，在其它人身上发现同样的生活动力、
精神基础，及个人特质之间的类似联系。不以统一标准衡量所有人；让人实现内心的和谐。强调一对一的关系。
3. 对待变化的态度: 将变化看作有条不紊、按部就班的过程。更适合稳定的环境。安全感来自尽早阻止它。
4. 模型关键字: 种子、选择、发现

对事件:有系统性思考。 认为某事件是由数以万计的其他因素作用的结果，包括时间的流逝
定义个人:考虑人的惟一性，即共性加个性
对关系的定义：将关系定义为一个独特的个体和另一个独特的个体之间的联系
面对改变的态度:期待和接受改变，将改变视为守宙的普通一部分，甚至欢迎改变，将其视为进入未知和成长领域的机会

    线性(linear)模型: 结果必有原因，原因必有结果；
1. 价值在于：大量的事件可以找到单一原因。
2. 希望把人分门别类，高效与人打交道；能组织大规模行动。
3. 对待变化的态度: 某个时刻不可能仅仅改变一点。安全感来自承担风险和容忍不确定性。

对事件:假定事件之间是线性关系
定义个人:将个人分门别类
对关系的定义：按照角色而非个人来定义关系
面对改变的态度:将改变看成是有序的，一次仅改变一件事的过程。在相对稳定的环境中非常有效

    惩罚/奖励(thread/reward)模型：带有道德色彩的线性模型 —— 答案有且只有一个。
1. 人的行为完全由面对的威胁和奖励所决定；权利植根于角色，而不是关系，钟情于头衔。强调权威
2. 安全感来自竭力保持所有人和关系不变化，或把变化转嫁给其他人（"改掉""不良"行为）。
    模型关键字: 力量、决断

第二章 领导方式模型

领导人的MOI模型：
M：激励(motivation)——奖励或是磨难，用来激励或惩罚相关的人
O：组织(organization)——现实的组织结构，将想法化为实践
I：头脑或创新(ideas or innovation)——种子，对未来图景的想象

强调创新的技术领导的三个关注点：
理解问题（understanding the problem）
    1）细心阅读规范（成败往往取决于问题定义的细小差异）
    2）鼓励成员认真阅读规范（鼓励其他人这么做是通过激励来实战的领导力）
    3）解决分歧时，要回到最初的问题（许多冗长争论的对象并非方案的相关价值，而是对问题的不同理解）
    4）从客户处获得规范的清晰定义和额外信息（有时候，不经意的交流就可能带来巨大的回报）
    5）工作一段之后，能更好理解某些需求的意义时，回头看看规范（高效有力的领导通过持续的测试来检查自己对工作的理解。它们固然自信，但也清楚自己的思维局限。）

管理思维（managing the flow of ideas）
    1）为团队贡献一个明智的想法（同样的思想在世界上出现，不是一次，也不是两次，而是无数次。比明智的想法更重要的是创造一个环境，只要真正能解决问题的想法一冒出来，就能被识别）
    2）鼓励借鉴有用的想法（大多数'新'的想法其实源自其他场合的旧想法）
    3）完善团队中其他人提出的想法（任何想法在刚刚形成的时候都不是完美的，即使是直接拿来套用的想法，也要修改才能适应新的环境）
    4）在团队希望完善的想法面前，放弃自己的想法；如果一个想法不是每个人都懂得，就不要放弃
    5）顶住时间压力，在其他人解释他们的想法时要耐心听取（在能够听取各种想法的环境中，项目进展更快，哪怕这些想法看来并不适用，项目的进展都要快一些）
    6）尝试其他人提出的想法（如果一个想法看来不错，他们就准备迅速地抓住，加以改善）
    7）为了让想法不断涌现出来，不要太早否定同事的想法（否定是一回事，轻率的否定是另一回事）
    8）如果必须批评一个想法，五笔明确，批评的对象是想法而不是人（不是每个想法都适用于每个问题，但是每个人都是有用的。在做否定的时候要很小心，斟酌要说的话，只否定想法，不否定个人）
    9）在给出想法之前，要试一试（没人能聪明到一人拥有全部的好想法；滔滔不绝地讲述未经深思熟虑的想法，倒是会大大妨碍其他人的思维）
    10）如果时间和人力不够，就别费力去寻求新想法，维持现状就好（上帝只用了六天创造世界，之后就不再创造新的物种）
    11）如果之前成功的想法不能适应新的形势，要鼓励团队放弃它（香蕉冰激淋，一天吃三次也会吃腻的）
    12）如果先前放弃的想法对问题的某个部分有价值，要重新采纳（想法本身没有好坏，只有在不适合的时间，不适合的地点出现的想法，对时机的把握要非常好）

保证质量（mantaining quality）
    1）在项目进展中测量质量（如果你没有解决要解决的问题，其他问题都毫无意义）
    2）在设计方案时，要设计工具和程序来衡量质量（衡量真实进度和质量看似项目的负担，但是好的工具能创造出一个环境，让质量控制变得简单自然）
    3）衡量实施速度，比照计划，随时准备改变解决方案（很多高科技企业就是因为产品面世迟了几个月而惨遭淘汰）
    4）脱离项目来观察，更新自己的看法，评估项目的生存能力（必然要失败的项目结束的越早，节省的资金就越多）
    5）在实践想法之前，要与客户一起检验（真正的领导更喜欢成功，独来独往只是附加品。如果客户不付钱，项目就谈不上成功）
    6）如果想法是错的，要重振士气（失败就是摆脱错误想法的束缚，是一种解脱，由此开始新一轮的创造＼实施和检验，而且这次额的效率会更高）


第三章 解决问题的方式
理解问题，创造所有人都能明白问题的环境的几个常见做法：
    1）细心阅读规范
    2）鼓励成员认真阅读规范
    3）解决分歧时，要回到最初的问题
    4）从客户处获得规范的清晰定义和额外信息
    5）工作进行一段之后，能更好理解某些需求的意义时，回头看看规范

驾驭新想法的十二个典型办法：
    1）为团队贡献一个明智的想法(Aristotle:It is not once,nor twice,but times without number that the same idea makes its appearance in the world)
    2）鼓励借鉴有用的想法
    3）完善团队中其他人提出的想法
    4）在团队希望完善的想法面前，放弃自己的想法：如果一个想法不是每个人都懂得，就不要放弃
    5）顶住时间压力，在其他人解释他们的想法的时候要耐心听取
    6）尝试其他人提出的想法
    7）为了让想法不断涌现出来，不要太早否定同事的想法
    8）如果批评一个想法，无比明确，批评的对象是想法而不是人
    9）在给出想法之前，要试一试
    10）如果时间和人力不够，就别费力去寻求新想法，维持现状就好
    11）如果之前成功的想法不能适应新的形势，要鼓励团队放弃它
    12）如果之前放弃的想法对问题的某个部分由价值，要重新采纳

通过以下办法来控制环境，保证质量：
    1）在项目进展中测量质量
    2）在设计方案时，要设计工具和程序来衡量质量
    3）衡量实施速度，比照计划，随时准备改变解决方案
    4）脱离项目来观察，更新自己的看法，评估项目的生存能力
    5）在实践想法之前，要与客户一起检验
    6）如果想法是错的，要重振士气


第四章 领导的成长
    领导成长模型：
    这个模型中存在两个主要阶段：慢速增长阶段(高原阶段:plateaus)和快速增长阶段(低谷阶段:ravines)，
他们轮流出现，不分先后。练习是慢速增长的一部分，同时在一些少数突破里产生了重大进展，同时在努力进步
的时候，必须要稍稍退下来一些，跌入低谷，才能取得重大进步。而真实的成长里面有大量的随机因素，个人
很容易被这些噪声所覆盖，所以这里对个人的要求也很高。

