第五章 我做不到，因为...
    常见的，逃避成为技术领导的痛苦的理由：
    1）'我不是主管'，认为只有在位的领导(Leader)才能管事，造成这个观念的原因是使用了传统的惩罚/奖励模型，
这个模型下领导力和管理是等同的。而在全面模型里面，任何团队都是个系统，割裂开是无法从整体上理解的。从而
大部分人认为被任命的领导就是团队中最重要的元素；另一方面人们很容易混淆'最薄弱'和'最关键'的环节，从而
信任被任命领导。
    在涉及良好的工作团队里，领导力来自任何人，而不是被任命的领导。因此你不需要等待——你也不应该去等待——
任命。
    2）'我不是领导那类人'，任何人都能掌握激励、组织和创新。尤其是创新是每个人都能够做到的事情，掌握
一点依靠创新来领导的技巧，也能增强激励型领导力和组织型领导力。，成为解决问题的领导，无论他是否觉得
自己是当领导的那类人。
    3）'我的技术会退步'，降低一些创新技能，换取一些激励和组织能力。这种这种无可避免，或许也是他们必须
做的最艰难的选择。
    4）'成长的压力很大'，不要给自己压力，知道环境所迫才改变的方法并不坏，没有什么方式比它更高明，除非
你设定了某些必须努力完成的目标。当领导，不一定要有个头衔。
    5）'我不用那么强大'技术之路永无止境，这种方法只是很基础的方法，在日常问题，例如生活、自由以及对
幸福追求上应用这种更平凡的领导方式，能够收获更多的能力。


