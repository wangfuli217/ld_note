#include "print.h"
void print_empty_rhombus(void)
{
	printf("        *\n");
	printf("       * *\n");
	printf("      *   *\n");
	printf("       * *\n");
	printf("        *\n");
}
void print_full_rhombus(void)
{
	printf("        *\n");
	printf("       ***\n");
	printf("      *****\n");
	printf("       ***\n");
	printf("        *\n");
}
