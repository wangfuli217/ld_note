#include <syslog.h>
#include <stdio.h>

//----------------------------------------------------
//	日志多涉及到一些异常的问题，
//	主要用来在长时间内跟踪程序的
//	执行，
//	要减少程序中日志的书写，它不是
//	想写就写，只有出现异常的情况下才写
//---------------------------------------------------
int main(void)
{
    FILE *f;

    f = fopen("not_here","r");
    if(!f)
        syslog(LOG_ERR|LOG_USER,"oops - %m\n");
    exit(0);
}
