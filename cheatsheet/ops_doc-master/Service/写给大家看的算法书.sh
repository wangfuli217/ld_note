1. 算法是对特定问题的解决步骤（对信息进行排序、搜索目标信息等）；
2. 算法→更优质的算法→好的程序；
3. 算法的两个必要条件：
    准确性，证明方法——断言；
    可停止性（死循环不能看做算法）；
    
几种重要的算法：

book(数论算法){
