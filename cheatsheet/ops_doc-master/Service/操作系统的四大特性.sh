OS(并发){
1. OS具有处理和调度多个程序同时执行的能力。进程的目的就是为了使程序能并发执行。
2. 并行性：两个或多个事件在同一时刻发生；
   并发性：两个或多个事件在同一时间间隔内发生；
}

