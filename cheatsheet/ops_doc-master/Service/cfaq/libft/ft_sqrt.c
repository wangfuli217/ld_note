#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 2147395600)
		return (0);
	while (nb > 3 && (i * i < nb))
		i++;
	if ((nb / i) == i)
		return (i);
	return (0);
}
