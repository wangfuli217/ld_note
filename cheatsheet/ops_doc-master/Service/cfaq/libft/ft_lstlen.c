#include "libft.h"

size_t		ft_lstlen(t_list *lst)
{
	t_list	*lst2;
	size_t	len;

	len = 0;
	lst2 = lst;
	while (lst2 && len++)
		lst2 = lst2->next;
	return (len);
}
