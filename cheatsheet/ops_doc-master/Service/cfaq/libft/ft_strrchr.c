#include "libft.h"

char	*ft_strrchr(const char *s1, int c)
{
	int i;

	i = 0;
	while (s1[i] && s1[i] != c)
		i++;
	while (s1[i])
		i++;
	while (s1[i] != c)
	{
		if (s1[i] == s1[0])
			return (NULL);
		i--;
	}
	return ((char*)&s1[i]);
}
